def patch_file(filename: str, body: str, description: str = None, official: list = None):
    path = ROOT / filename
    html = path.read_text(encoding="utf-8")
    if description:
        html = re.sub(r'<meta name="description" content="[^"]*"', f'<meta name="description" content="{description}"', html)
        html = re.sub(r'<meta property="og:description" content="[^"]*"', f'<meta property="og:description" content="{description}"', html)
    html = re.sub(
        r'<article class="article-content">.*?<div class="official-links">',
        f'<article class="article-content">\n{body}\n<div class="official-links">',
        html,
        count=1,
        flags=re.S,
    )
    if official:
        links = "\n".join(f'<li><a href="{u}" rel="noopener noreferrer" target="_blank">{t}</a></li>' for t, u in official)
        html = re.sub(
            r'<div class="official-links">.*?<p>신청 요건',
            f'<div class="official-links">\n<h2>공식 확인 링크</h2>\n<ul>\n{links}\n</ul>\n<p>신청 요건',
            html,
            count=1,
            flags=re.S,
        )
    path.write_text(html, encoding="utf-8")
    print(f"patched: {filename}")


